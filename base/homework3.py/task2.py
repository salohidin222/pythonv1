import math

num1 = int(input("Введите первое число: "))
num2 = int(input("Введите второе число: "))
num3 = int(input("Введите третье число: "))
sqrt_num1 = math.sqrt(num1)
sqrt_num2 = math.sqrt(num2)
sqrt_num3 = math.sqrt(num3)

print(f"Квадратный корень из {num1} равен {sqrt_num1}")
print(f"Квадратный корень из {num2} равен {sqrt_num2}")
print(f"Квадратный корень из {num3} равен {sqrt_num3}")
